x = 10
y = 12
# Greater than
print('x > y is', x > y)       # Output: x > y is False
# Less than
print('x < y is', x < y)       # Output: x < y is True
# Equals to
print('x == y is', x == y)     # Output: x == y is False
# Not equals to
print('x != y is', x != y)     # Output: x != y is True
# Greater than or equals to
print('x >= y is', x >= y)     # Output: x >= y is False
# Less than or equals to
print('x <= y is', x <= y)     # Output: x <= y is True